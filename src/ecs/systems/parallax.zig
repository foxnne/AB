const std = @import("std");
const zm = @import("zmath");
const ecs = @import("zflecs");
const game = @import("../../ab.zig");
const components = game.components;

pub fn system() ecs.system_desc_t {
    var desc: ecs.system_desc_t = .{};
    desc.query.filter.terms[0] = .{ .id = ecs.id(components.Position) };
    desc.query.filter.terms[1] = .{ .id = ecs.id(components.Parallax) };
    desc.run = run;
    return desc;
}

pub fn run(it: *ecs.iter_t) callconv(.C) void {
    while (ecs.iter_next(it)) {
        var i: usize = 0;
        while (i < it.count()) : (i += 1) {
            if (ecs.field(it, components.Position, 1)) |positions| {
                if (ecs.field(it, components.Parallax, 2)) |parallaxes| {
                    if (game.state.camera.velocity != 0.0) {
                        positions[i].x += parallaxes[i].value * game.state.camera.velocity;
                    }
                }
            }
        }
    }
}
